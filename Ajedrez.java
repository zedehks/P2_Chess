    public static void cleanGlows()
    {
        int j = layers.getComponentCountInLayer(JLayeredPane.MODAL_LAYER);
        //System.out.println(j);
        for (int i = 0; i < j; i++)
        {
            layers.remove(layers.getComponentsInLayer(JLayeredPane.MODAL_LAYER)[0]);  
        }
        layers.repaint();
    }
    
    

